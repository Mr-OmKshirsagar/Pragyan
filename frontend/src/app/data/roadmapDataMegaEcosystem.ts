import { ExpandedRoadmap, RoadmapCategory } from '../types/roadmapExpanded';

// ============ MEGA ECOSYSTEM: 200+ ROADMAPS ============

// Type extensions for the mega ecosystem
export interface MegaRoadmap extends ExpandedRoadmap {
  tags: string[];
  subcategory?: string;
  careerPaths?: string[];
  xpReward?: number;
  popularity?: number;
  trending?: boolean;
}

export interface MegaRoadmapCategory extends RoadmapCategory {
  subcategories?: string[];
  roadmapCount?: number;
}

// ============ HELPER: Generate roadmaps dynamically ============

interface RoadmapTemplate {
  title: string;
  category: string;
  subcategory?: string;
  icon: string;
  description: string;
  level: 'beginner' | 'intermediate' | 'advanced' | 'expert';
  duration: string;
  weeks: number;
  dailyTasks: number;
  tags: string[];
  careerPaths?: string[];
  prerequisites?: string[];
  relatedSkills?: string[];
}

function createRoadmap(template: RoadmapTemplate, index: number): MegaRoadmap {
  const taskCount = template.dailyTasks * template.weeks;
  const estimatedHours = template.weeks * 8;

  return {
    id: `${template.category}-${template.title.toLowerCase().replace(/\s+/g, '-')}-${index}`,
    title: template.title,
    category: template.category,
    subcategory: template.subcategory,
    icon: template.icon,
    description: template.description,
    level: template.level,
    duration: `${template.weeks} weeks`,
    totalTasks: taskCount,
    estimatedHours,
    color: 'primary',
    learningOutcomes: [
      `Master ${template.title}`,
      `Build real-world projects`,
      `Understand best practices`,
      `Career-ready skills`,
    ],
    tags: template.tags,
    careerPaths: template.careerPaths || [],
    prerequisites: template.prerequisites || [],
    relatedSkills: template.relatedSkills || [],
    xpReward: taskCount * 10,
    popularity: Math.floor(Math.random() * 100),
    trending: Math.random() > 0.7,
    weeks: Array.from({ length: template.weeks }, (_, i) => ({
      week: i + 1,
      title: `Week ${i + 1}: Advanced Topics`,
      topics: Array.from({ length: 3 }, (_, j) => ({
        title: `Topic ${j + 1}`,
        description: 'Learn and practice key concepts',
      })),
      estimatedHours: 8,
    })),
  };
}

