export function searchMegaRoadmaps(query: string): MegaRoadmap[] {
  const lowercaseQuery = query.toLowerCase();
  return megaEcosystemRoadmaps.filter(
    (r) =>
      r.title.toLowerCase().includes(lowercaseQuery) ||
      r.description.toLowerCase().includes(lowercaseQuery) ||
      r.tags.some((tag) => tag.toLowerCase().includes(lowercaseQuery)) ||
      r.learningOutcomes.some((outcome) => outcome.toLowerCase().includes(lowercaseQuery)) ||
      (r.careerPaths && r.careerPaths.some((path) => path.toLowerCase().includes(lowercaseQuery)))
  );
}

export function getMegaRoadmapsByCategory(categoryId: string): MegaRoadmap[] {
  return megaEcosystemRoadmaps.filter((r) => r.category === categoryId);
}

export function getMegaRoadmapsByLevel(level: 'beginner' | 'intermediate' | 'advanced' | 'expert'): MegaRoadmap[] {
  return megaEcosystemRoadmaps.filter((r) => r.level === level);
}

export function getMegaRoadmapsByTag(tag: string): MegaRoadmap[] {
  return megaEcosystemRoadmaps.filter((r) => r.tags.includes(tag.toLowerCase()));
}

export function getTrendingRoadmaps(): MegaRoadmap[] {
  return megaEcosystemRoadmaps.filter((r) => r.trending).sort((a, b) => (b.popularity || 0) - (a.popularity || 0)).slice(0, 10);
}

export function getPopularRoadmaps(): MegaRoadmap[] {
  return megaEcosystemRoadmaps.sort((a, b) => (b.popularity || 0) - (a.popularity || 0)).slice(0, 15);
}

export function getMegaCategoryColor(categoryId: string): string {
  const cat = megaRoadmapCategories.find((c) => c.id === categoryId);
  return cat?.color || '#6B7280';
}

export function getMegaCategoryName(categoryId: string): string {
  const cat = megaRoadmapCategories.find((c) => c.id === categoryId);
  return cat?.name || 'Unknown';
}

export function getAllMegaRoadmaps(): MegaRoadmap[] {
  return megaEcosystemRoadmaps;
}

export function getTotalRoadmapCount(): number {
  return megaEcosystemRoadmaps.length;
}

export function getCategoryStats(): { categoryId: string; categoryName: string; count: number }[] {
  const stats = new Map<string, number>();
  megaEcosystemRoadmaps.forEach((r) => {
    stats.set(r.category, (stats.get(r.category) || 0) + 1);
  });
  return Array.from(stats.entries()).map(([categoryId, count]) => ({
    categoryId,
    categoryName: getMegaCategoryName(categoryId),
    count,
  }));
}

export function getAllUniqueTags(): string[] {
  const tags = new Set<string>();
  megaEcosystemRoadmaps.forEach((r) => {
    r.tags.forEach((tag) => tags.add(tag));
  });
  return Array.from(tags).sort();
}
